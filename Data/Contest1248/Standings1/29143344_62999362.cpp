template <class T>
inline bool read(T& x) {
	bool flg = false;
	char dd;
	x = 0;
	for (; !isdigit(c); dd) {
		if (c == '-') {
			flg = true;
		} else if(c == EOF) {
			return false;
		}
	}
	for (; isdigit(c); dd) {
		x = (x << 1) + (x << 3) + (c ^ 48);
	}
	if (flg) {
		x = -x;
	}
	return true;
}
#undef dd

template <class T>
inline void write(T x) {
	if (x < 0) {
		putchar('-');
		x = -x;
	}
	if (x < 10) {
		putchar(x | 48);
		return;
	}
	write(x / 10);
	putchar((x % 10) | 48);
}

template <class T>
inline void writesp(T x) {
	write(x);
	putchar(' ');
}

template <class T>
inline void writeln(T x) {
	write(x);
	puts("");
}

typedef long long LL;

const int maxn = 505;

int n;
char s[maxn];
int f[maxn];
int nc[maxn];

inline int nxt(int x) {
	x++;
	if (x > n) {
		x -= n;
	}
	return x;
}

inline int get() {
	int ans = 0;
	for (int i = 1; i <= n; ++i) {
		if (s[i] == '(') {
			nc[i] = nc[i - 1] + 1;
			f[i] = f[i - 1];
		} else if (nc[i - 1] > 0) {
			nc[i] = nc[i - 1] - 1;
			f[i] = f[i - 1];
		} else {
			nc[i] = nc[i - 1];
			f[i] = f[i - 1] + 1;
		}
	}
	int nowcnt = 0;
	int nowf = 0;
	for (int i = n; i; --i) {
		if (s[i] == ')') {
			nowcnt++;
		} else if (nowcnt > 0) {
			nowcnt--;
		} else {
			nowf++;
		}
		if (!nowcnt && !nc[i - 1] && nowf == f[i - 1]) {
			ans++;
		}
	}
	return ans;
}

int main() {
	scanf("%d%s", &n, s + 1);
	int ans = get();
	int xx = 1, yy = 1;
	for (int i = 1; i <= n; ++i) {
		for (int j = i + 1; j <= n; ++j) {
			if (s[i] != s[j]) {
				swap(s[i], s[j]);
				int anss = get();
				if (anss > ans) {
					ans = anss;
					xx = i, yy = j;
				}
				swap(s[i], s[j]);
			}
		}
	}
	writeln(ans);
	writesp(xx), write(yy);
	return 0;
}