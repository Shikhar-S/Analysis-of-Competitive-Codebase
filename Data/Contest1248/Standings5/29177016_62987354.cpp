#include <bits/stdc++.h>

using namespace std;

int arr[100005];

int main()
{
#ifdef LOCAL
	freopen("input.txt","rt",stdin);
#endif

	int n;
	cin>>n;
	for (int i=0;i<n;i++)
        cin>>arr[i];
    sort(arr,arr+n);
    long long a=0,b=0;
    for (int i=0;i<n>>1;i++)
        a+=arr[i];
    for (int i=n>>1;i<n;i++)
        b+=arr[i];
    long long ans=a*a+b*b;
    cout<<ans<<endl;
	return 0;
}

