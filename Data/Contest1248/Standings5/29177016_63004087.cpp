#include <bits/stdc++.h>

using namespace std;

int arr1[100005]={2,2,4};
int arr2[100005]={0,0,2};
int arr3[100005]={0,0,2};

int main()
{
#ifdef LOCAL
	freopen("input.txt","rt",stdin);
#endif

	int n,m;
	cin>>n>>m;
	for (int i=3;i<100005;i++)
    {
        arr1[i]=(arr1[i-1]+arr1[i-2])%1000000007;
        arr2[i]=(arr2[i-1]+arr2[i-2])%1000000007;
        arr3[i]=(arr3[i-1]+arr2[i])%1000000007;
    }
    cout<<(arr1[n]+arr3[m])%1000000007<<endl;
	return 0;
}

