const int N = 200005;
int a[N];


void solve(int n)
{
	for(int i=1;i<=n;i++)
		cin>>a[i];

	sort(a+1,a+n+1);

	if(n%2)
	{
		int m = n/2 + 1;

		ll s1=0, s2=0;

		for(int i=1;i <= n/2;i++)
			s1+=a[i];
		
		for(int i=m;i<=n;i++)
			s2+=a[i];

		//cout<<s1 <<" "<<s2 <<"\n";

		 cout<< (s1*s1 + s2*s2) <<"\n";

	}
	else
	{
		int m = n/2;

		ll s1=0, s2=0;

		for(int i=1;i <= n/2;i++)
			s1+=a[i];
		
		for(int i=m+1;i<=n;i++)
			s2+=a[i];

		//cout<<s1 <<" "<<s2 <<"\n";
	

		cout<< (s1*s1 + s2*s2) <<"\n";
	}

}


