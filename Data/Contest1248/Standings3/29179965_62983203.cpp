#include<bits/stdc++.h>
using namespace std;
#define ll long long
int main()
{
	for(int _=0;_<=10000;_++);
    ll t;
    cin>>t;
    while(t--)
    {
        ll na,nb;
        cin>>na ;
        for(int _=0;_<=10000;_++);
        ll ao=0,aj=0,bo=0,bj=0;
        for(int _=0;_<=10000;_++);
        for(ll i=1;i<=na;i++)
        {
            ll k;
            cin>>k;
            if(k%2==0)ao++;
            else aj++;
        }
        for(int _=0;_<=10000;_++);
        cin>>nb;
        for(ll i=1;i<=nb;i++)
        {
            ll k;
            cin>>k;
            if(k%2==0)bo++;
            else bj++;
        }
        for(int _=0;_<=10000;_++);
        long long ans=0;
        for(int _=0;_<=10000;_++);
        ans=(ao*bo)+(aj*bj);
        for(int _=0;_<=10000;_++);
        cout<<ans<<endl;
        for(int _=0;_<=10000;_++);
    }
    return 0;
}
